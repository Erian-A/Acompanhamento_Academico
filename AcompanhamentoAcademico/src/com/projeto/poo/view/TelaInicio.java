    private void botaoConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoConfirmarActionPerformed
        // TODO add your handling code here:
        int numSemestre = 0; 
        String codigoDiscp = this.textCodigoDisciplina.getText();
        Disciplina discp = this.controlador.RetornarDisciplinaInstituicao(codigoDiscp);
        String comboBox = this.comboBoxNumSemestre.getSelectedItem().toString();
        
        if (!comboBox.equals("Semestre")){
            numSemestre = Integer.parseInt(comboBox);
        }
        
        
        if (this.controlador.VerificarRequisitos(this.alunoLogado.getGrade(), discp)){
            this.alunoLogado.addDisciplinaGrade(discp, numSemestre);
            this.labelResultadoOperacao.setText("Disciplina adicionada na grade!!");
        }
        else{
            JOptionPane.showMessageDialog(null, "Você não possui os requisitos para cursar essa disciplina!", "ERRO", JOptionPane.WARNING_MESSAGE);
            this.labelResultadoOperacao.setText("");
        }
        
        
        
    }//GEN-LAST:event_botaoConfirmarActionPerformed

    private void menuItemPerfilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_menuItemPerfilActionPerformed
        // TODO add your handling code here:
        this.labelNome.setText(this.alunoLogado.getNome());
        this.labelMatricula.setText(this.alunoLogado.getMatricula());
        this.labelEmail.setText(this.alunoLogado.getEmail());
        this.labelCreditosNum.setText("");
        this.labelCreditosPorcentagem.setText("");
        this.comboBoxHistorico.removeAllItems();
        this.comboBoxHistorico.addItem("Historico");
        
        
        DefaultTableModel model = (DefaultTableModel) this.tabelaSemestreAtual.getModel();
        RemoverLinhasTabela(model);

        this.tabelaSemestreAtual.getColumnModel().getColumn(1).setPreferredWidth(200);
        this.tabelaSemestreAtual.getColumnModel().getColumn(0).setHeaderValue("Código");
        this.tabelaSemestreAtual.getColumnModel().getColumn(1).setHeaderValue("Nome");
        this.tabelaSemestreAtual.getColumnModel().getColumn(2).setHeaderValue("Professor");
        this.tabelaSemestreAtual.getColumnModel().getColumn(3).setHeaderValue("Créditos");
        this.tabelaSemestreAtual.getTableHeader().repaint();
        
        
        c1.show(jPanel1, "cardPerfil");
        
        
        
        
        for (int i = 1; i < this.numeroSemestreAtual; i++){
            this.comboBoxHistorico.addItem(String.valueOf(i));
        }
        
        
        if (this.alunoLogado.getGrade().getSemestres().size() == this.numeroSemestreAtual){
            
            if (this.alunoLogado.getSemestreAtual() != null){
                HashMap<String, Disciplina> discp = this.alunoLogado.getSemestreAtual().getDisciplinasSemestre();
                for (String codigo : discp.keySet()){
                    Object[] row = {discp.get(codigo).getCodigo(), discp.get(codigo).getNome(), discp.get(codigo).getProfessor(), discp.get(codigo).getQntCreditos()};
                    model.addRow(row);
                }    
            }
        }
        if (this.numeroSemestreAtual != 0){
            this.labelCreditosNum.setText(String.valueOf(this.controlador.creditosCursados("CDC", this.alunoLogado.getNome())) + " créditos");
            this.labelCreditosPorcentagem.setText(String.valueOf(this.controlador.porcentagemCreditosCursados("CDC", this.alunoLogado.getNome())) + " %");    
        }
        else{
            this.labelCreditosNum.setText("0");
            this.labelCreditosPorcentagem.setText("0 %");
        }
        
        
    }//GEN-LAST:event_menuItemPerfilActionPerformed

    private void botaoHistoricoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoHistoricoActionPerformed
        // TODO add your handling code here:
        int numSemestre = 0; 
        
        String comboBox = this.comboBoxHistorico.getSelectedItem().toString();
        if (!this.comboBoxHistorico.getSelectedItem().toString().equals("Historico")){
            numSemestre = Integer.parseInt(comboBox);
            JFrame f = new JFrame("Histórico - " + numSemestre + "° Semestre");
            Object[] columNames = {"Código", "Nome", "Professor", "Créditos"};

            DefaultTableModel data = new DefaultTableModel(columNames, 0);

            HashMap<String, Disciplina> discp = this.alunoLogado.getGrade().RetornarSemetre(numSemestre).getDisciplinasSemestre(); 

            for (String codigo : discp.keySet()){
                Object[] row = {discp.get(codigo).getCodigo(), discp.get(codigo).getNome(), discp.get(codigo).getProfessor(), discp.get(codigo).getQntCreditos()};
                data.addRow(row);
            }

            JTable table = new JTable(data);
            table.getColumnModel().getColumn(1).setPreferredWidth(230);
            JScrollPane scrollTable = new JScrollPane(table);
            scrollTable.setViewportView(table);
            f.add(new JScrollPane(table));
            f.setSize(300, 200);
            f.pack();
            f.setLocationRelativeTo(null);
            f.setVisible(true);
        }
                  

        
    }//GEN-LAST:event_botaoHistoricoActionPerformed

