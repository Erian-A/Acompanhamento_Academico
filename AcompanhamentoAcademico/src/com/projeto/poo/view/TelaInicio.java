    private void botaoTabelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoTabelaActionPerformed
        // TODO add your handling code here:
        if (this.jTable1.getColumnCount() != 4){
            TableColumn aColumn = new TableColumn();
            this.jTable1.addColumn(aColumn);
        }
        DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();
        RemoverLinhasTabela(model);
        // Tentar pegar um listener para verificar mudanca
        if (this.comboBox_I.getSelectedItem().toString().equals("Disciplinas - Instituição") ){
            
            this.labelTituloTabela.setText("Disciplinas - Instituição");
            
            this.jTable1.getColumnModel().getColumn(1).setPreferredWidth(200);
            this.jTable1.getColumnModel().getColumn(0).setHeaderValue("Código");
            this.jTable1.getColumnModel().getColumn(1).setHeaderValue("Nome");
            this.jTable1.getColumnModel().getColumn(2).setHeaderValue("Professor");
            this.jTable1.getColumnModel().getColumn(3).setHeaderValue("Créditos");
            this.jTable1.getTableHeader().repaint();            
            
            
            
            HashMap<String, Disciplina> discp = this.controlador.getInstituicao().getDisciplinas(); 
            for (String codigo : discp.keySet()){
                Object[] row = {discp.get(codigo).getCodigo(), discp.get(codigo).getNome(), discp.get(codigo).getProfessor(), discp.get(codigo).getQntCreditos()};
                model.addRow(row);
            }
            
        }
        else if (this.comboBox_I.getSelectedItem().toString().equals("Disciplinas - Semestre") ){
            
            int numSemestre = Integer.parseInt(this.textPesquisa.getText());
            Semestre semestre = this.controlador.RetornarSemestreGrade(numSemestre, "CDC");
            
            this.labelTituloTabela.setText("Disciplinas - Semestre " + numSemestre);
            
            this.jTable1.getColumnModel().getColumn(1).setPreferredWidth(200);
            this.jTable1.getColumnModel().getColumn(0).setHeaderValue("Código");
            this.jTable1.getColumnModel().getColumn(1).setHeaderValue("Nome");
            this.jTable1.getColumnModel().getColumn(2).setHeaderValue("Professor");
            this.jTable1.getColumnModel().getColumn(3).setHeaderValue("Créditos");
            this.jTable1.getTableHeader().repaint();
            
            HashMap<String, Disciplina> discp = semestre.getDisciplinasSemestre(); 
            for (String codigo : discp.keySet()){
                Object[] row = {discp.get(codigo).getCodigo(), discp.get(codigo).getNome(), discp.get(codigo).getProfessor(), discp.get(codigo).getQntCreditos()};
                model.addRow(row);
            }
            
        }
        else if (this.comboBox_I.getSelectedItem().toString().equals("Disciplinas - Professor") ){
            this.jTable1.removeColumn(this.jTable1.getColumnModel().getColumn(3));
            String nomeProfessor = this.textPesquisa.getText();
            
            Professor professor = this.controlador.RetornarProfessor(nomeProfessor);
            
            if (professor != null){
                this.labelTituloTabela.setText("Disciplinas - Professor(a) " + professor.getNome());
            
                this.jTable1.getColumnModel().getColumn(1).setPreferredWidth(200);
                this.jTable1.getColumnModel().getColumn(0).setHeaderValue("Código");
                this.jTable1.getColumnModel().getColumn(1).setHeaderValue("Nome");
                this.jTable1.getColumnModel().getColumn(2).setHeaderValue("Créditos");
                this.jTable1.getTableHeader().repaint();

                HashMap<String, Disciplina> discp = professor.getDisciplinasLecionadas(); 
                for (String codigo : discp.keySet()){
                    Object[] row = {discp.get(codigo).getCodigo(), discp.get(codigo).getNome(), discp.get(codigo).getQntCreditos()};
                    model.addRow(row);
                }

            }
            
        }
        else {
            this.jTable1.getColumnModel().getColumn(0).setHeaderValue("-");
            this.jTable1.getColumnModel().getColumn(1).setHeaderValue("-");
            this.jTable1.getColumnModel().getColumn(2).setHeaderValue("-");
            this.jTable1.getColumnModel().getColumn(3).setHeaderValue("-");
            this.jTable1.getTableHeader().repaint();
        }
    }//GEN-LAST:event_botaoTabelaActionPerformed

