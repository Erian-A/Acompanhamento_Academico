    private void botaoDefinirSemestreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoDefinirSemestreActionPerformed
        // TODO add your handling code here:
        this.numeroSemestreAtual = Integer.parseInt(this.textFieldSemestreAtual.getText());
        
        
        this.comboBoxNumSemestre.removeAllItems();
        
        this.comboBoxNumSemestre.addItem("Semestre");
        for (int j = 1; j <= this.numeroSemestreAtual; j++){
            this.comboBoxNumSemestre.addItem(String.valueOf(j));
        }
        
        for (int i = 1; i <= this.numeroSemestreAtual; i++){
            
            Semestre semestre = new Semestre(i);
                
            this.alunoLogado.getGrade().AdicionarSemestreGrade(semestre);
            
        }
        this.controlador.DefinirSemestreAtual(numeroSemestreAtual, this.alunoLogado.getNome());

    }//GEN-LAST:event_botaoDefinirSemestreActionPerformed

    private void botaoCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCadastroActionPerformed
        // TODO add your handling code here:
        String nome = this.textFieldNome.getText();
        String email = this.textFieldEmail.getText();
        String matricula = this.textFieldMatricula.getText();
        String tipoCadastro = this.comboBoxCadastro.getSelectedItem().toString();
        
        if (tipoCadastro.equals("Aluno")){
            
            this.controlador.AdicionarAlunoInstituicao(nome, email, matricula);
            this.labelCadastro.setText("Cadastro realizado com sucesso!");
        }
        else if (tipoCadastro.equals("Professor")){
            this.controlador.AdicionarProfessorInsituicao(nome, email);
            this.labelCadastro.setText("Cadastro realizado com sucesso!");            
        }
        else {
            this.labelCadastro.setText("");
        }
        

    }//GEN-LAST:event_botaoCadastroActionPerformed

