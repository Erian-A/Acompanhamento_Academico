    private void botaoLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoLoginActionPerformed
        // TODO add your handling code here:
        String nomeLogin = this.textLogin.getText();
        
     
        this.comboBoxCadastro.removeAllItems();
        this.comboBoxCadastro.addItem("Tipo");
        this.comboBoxCadastro.addItem("Aluno");
        this.comboBoxCadastro.addItem("Professor");
        
        this.comboBox_I.removeAllItems();
        this.comboBox_I.addItem("Menu");
        this.comboBox_I.addItem("Disciplinas - Instituição");
        this.comboBox_I.addItem("Disciplinas - Semestre");
        this.comboBox_I.addItem("Disciplinas - Professor");
        this.textFieldSemestreAtual.setText("");
        this.textPesquisa.setText("");
        
        
        // Label da tabela no panel Pesquisar
        this.labelTituloTabela.setText("");
        
        // Limpar tabela no panel Pesquisar
        DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();
        RemoverLinhasTabela(model);
        
        
        // Limpar tabela no panel Perfil
        DefaultTableModel model_I = (DefaultTableModel) this.tabelaSemestreAtual.getModel();
        RemoverLinhasTabela(model_I);
        
        // Deixar formatacao basica
        this.jTable1.getColumnModel().getColumn(0).setHeaderValue("-");
        this.jTable1.getColumnModel().getColumn(1).setHeaderValue("-");
        this.jTable1.getColumnModel().getColumn(2).setHeaderValue("-");
        this.jTable1.getColumnModel().getColumn(3).setHeaderValue("-");
        this.jTable1.getTableHeader().repaint();
        
        
        // Limpar ComboBox
        this.comboBoxHistorico.removeAllItems();
        
        // Ajuste Panel Grade
        this.textFieldSemestreAtual.setText("");
        this.textCodigoDisciplina.setText("");
        this.comboBoxNumSemestre.removeAllItems();
        
        DefaultTableModel model_II = (DefaultTableModel) this.tableGrade.getModel();
        RemoverLinhasTabela(model_II);
        
        
        if (nomeLogin.equals("admin")){
            c1.show(jPanel1, "cardCadastro");
            this.textFieldNome.setText("");
            this.textFieldMatricula.setText("");
            this.textFieldEmail.setText("");
            this.labelCadastro.setText("");
            this.menuItemGrade.setVisible(false);
            this.menuItemAbaPesquisa.setVisible(false);
            this.menuItemPerfil.setVisible(false);
        }
        
        else if (this.controlador.VerificarAlunoMatriculado(nomeLogin)){
            c1.show(jPanel1, "cardPesquisa");
            this.menuItemGrade.setVisible(true);
            this.menuItemAbaPesquisa.setVisible(true);
            this.menuItemPerfil.setVisible(true);
            this.alunoLogado = this.controlador.RetornarAlunoMatriculado(nomeLogin);
            
            if (this.alunoLogado.getSemestreAtual() != null){
                this.numeroSemestreAtual = this.alunoLogado.getSemestreAtual().getnumeroSemetre();
            }
            else{
                this.numeroSemestreAtual = 0;
            }
        }
    }//GEN-LAST:event_botaoLoginActionPerformed

